/**
 * 一维数组转树形结构
 * @param data 一维数组
 * @param pid 父id
 * @param pidName 父id字段名
 * @param idName id字段名
 */
export function treeData(data, pid, pidName, idName) {
  var tree = [],
      temp;
  for (var i = 0; i < data.length; i++) {
    if (data[i][pidName] == pid) {
      var obj = data[i];
      temp = treeData(data, data[i][idName], pidName, idName);
      if (temp.length > 0) {
        obj.children = temp;
      }
      tree.push(obj);
    }
  }
  return tree;
}


